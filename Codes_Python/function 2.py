def f(n=9):
    print(n) # 5
f(5)

'''
1. Function Definition:
def f(n=9) defines a function f with one parameter n. The default value of n is set to 9, meaning if the function is called without an argument, n will default to 9.

2. Calling the Function:
f(5) calls the function f, passing the value 5 as the argument for n. This overrides the default value of n (which is 9).

3. Inside the Function:
Inside the function, print(n) prints the value of n, which is 5 in this case, because 5 was passed as the argument.
'''
